namespace Homespun.Components.Shared;

/// <summary>
/// Available modal sizes.
/// </summary>
public enum ModalSize
{
    /// <summary>Default size (no modifier class).</summary>
    Default,
    /// <summary>Small modal (modal-sm).</summary>
    Sm,
    /// <summary>Large modal (modal-lg).</summary>
    Lg,
    /// <summary>Extra-large modal (modal-xl).</summary>
    Xl
}

/// <summary>
/// Button variants for the confirm button.
/// </summary>
public enum ConfirmButtonVariant
{
    /// <summary>Primary/default blue button.</summary>
    Primary,
    /// <summary>Red danger button for destructive actions.</summary>
    Danger,
    /// <summary>Yellow warning button.</summary>
    Warning,
    /// <summary>Green success button.</summary>
    Success
}
